package pojo.response.Issue;

public class IssueRes {

	private String self;

	private String id;

	private String key;

	public void setSelf(String self) {
		this.self = self;
	}

	public String getSelf() {
		return self;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getId() {
		return id;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getKey() {
		return key;
	}

}
