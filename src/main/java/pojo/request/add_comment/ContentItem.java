package pojo.request.add_comment;

public class ContentItem {

	private String type;

	private String text;

	public void setType(String type) {
		this.type = type;
	}

	public String getType() {
		return type;
	}

	public void setText(String text) {
		this.text = text;
	}

	public String getText() {
		return text;
	}

}